<h1 class="head">свадебное торжество</h1>

<h2>1. на память (эконом)</h2>

<p>до 8 часов – 12 т.; до 12 – 15т.</p>
<p>сюда входит – </p>
<p>1) репортажная съёмка одной камерой всего дня и </p>
<p>2) монтаж отснятого материала в 2-х часовой (примерно) "фильм" с линейной (последовательной) структурой (например, для свадебного видео – начало, выкуп, регистрация, прогулка, банкет, финал).</p>
<p>часть материала монтируется по типу музыкального клипа, другая часть - "вживую", то есть "как есть", без мусора, с перебивками и переходами, по типу документального фильма.</p>

<h2>2. базовый (не экономь!)</h2>

<p>до 10 часов – 18 т.; до 12 часов – 21 т.</p>
<p>сюда входит – </p>
<p>1) репортажная съёмка одной камерой всего дня и</p>
<p>2) монтаж отснятого материала в двух вариантах – длинного и короткого фильма.</p>
<p>«длинный» фильм представляет собой хронику вашего события. здесь сохраняется практически весь смотрибельный видеоматериал, большая часть которого идёт «вживую», а часть монтируется по типу музыкального клипа.</p>
<p>«короткий» фильм – 9 – 12 минут практически полностью делается как клип с вставками «живого» звука и разными треками, подчёркивающими структуру ролика, эмоциональный фон и динамику.</p>

<h2>3. две – два (две камеры – два фильма)</h2>

<p>27 т.
<p>те же два фильма, но из большего объёма видеоматериала – съёмка двумя камерами в течение всего дня. плюс, бонус – в изготовлении «короткого» фильма возможно использование всевозможных архивов (домашнее фото, видео).</p>


