<div class="about-me__text-title">
Я верю в то, что красота не просто однажды спасет мир, <br> я верю, в то, что она его исцеляет каждую секунду.
</div>
<div class="about-me__text-note">
<p>Я верю в то, что жизнь, это ежедневный выбор – как воспринимать мир, <br> каким его видеть и как с ним взаимодействовать.</p>
<p>Я верю в магию дизайна: в чистоту линий, в гармонию пропорций, в свет и цвет, <br> в безупречность форм и легкость мыслей.</p>
<p>Я верю в стиль, в элегантный интерьер, я верю в вечные ценности.&nbsp;</p>
<p>Я верю в то, что работа дизайнера – это выбор: это простой выбор видеть мир красивым.</p>
                                    <p>Я верю в то, что правильный интерьер – это настроение и комфорт, это философия, <br> это спокойная уверенность вашего личного рая.</p>
                                    <p>Там за окнами очень быстрый мир, и это очень быстро меняющийся мир, и я верю в то, <br> что в жизни каждого человека должен быть уголок для восстановления мира в душе, <br> даже если этот мир напрокат.</p>
                                    <p>Я верю в то, что цветы должны быть свежесрезанными, а кофе очень черным.</p>
                                </div>
