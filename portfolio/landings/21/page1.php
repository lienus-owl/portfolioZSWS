<h1>Безопасность</h1>

<p>Биокамин Fire Line Automatic является самым безопасным камином в мире, что подтверждают специализированные сертификаты пожарной безопасности США, Европы и России. Он оснащен 16 электронными датчиками безопасности (включая газоанализатор, датчики контроля уровня топлива и влажности, температурные датчики, датчик наклона и сейсмоактивности) и микропроцессором, которые постоянно управляют устройством, а также встроенными вентиляторами для охлаждения камина. В случае любой опасности устройство издает сигнал предупреждения, информация выводится на дисплей, и огонь гаснет автоматически.</p>

