<p>Компания Planika разработала и запатентовала уникальную по своей чистоте технологию BEV (Burning Etanol Vapours) , при которой происходит сгорание не самого топлива, а паров, вырабатывающихся при его нагревании в закрытом топливном резервуаре. Данная технология на сегодняшний день является единственной запатентованной технологией на рынке биокаминов (Патент: PL 2011114223).</p>
