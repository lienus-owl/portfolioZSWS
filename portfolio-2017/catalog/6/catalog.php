					<h2>Дверной врезной замок: критерии выбора запорных механизмов</h2>

					<p>Все корпуса замков врезных имеют массу отличий, которые следует учесть на этапах подбора двери и запорного механизма. Выбирая врезные замки для дверей, нужно обратить внимание на несколько важных моментов:</p>
					<ul>
						<li>Назначение изделия – межкомнатные, для металлических дверей, гаражные, входные, подъездные и др.;</li>

						<li>Рекомендованная толщина и материал изготовления полотна – замки, как правило, предназначены для монтажа в двери толщиной от 30 до 70 мм, которые выполнены из пластика, металла или дерева;</li>

						<li>Тип механизма – можно подобрать замок врезной цилиндровый, а также изделия сувальдного типа с 2-4 поворотами ключа для достижения максимальной защиты от вскрытия/взлома;</li>

						<li>Специфика монтажа. Замочные изделия монтируются непосредственно в дверное полотно (чаще всего в уже готовое отверстие). Поэтому важно правильно определить размеры врезного замка и сопоставить их с параметрами полотна и технического отверстия;</li>

						<li>Материал изготовления – чаще всего используется прочная сталь. Также можно подобрать корпуса замков с пластиковым засовом, накладками из латуни или с защитными стальными пластинами.</li>
