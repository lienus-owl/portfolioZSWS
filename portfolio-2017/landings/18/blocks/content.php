	<h2>Pulsart I: Joris Voorn</h2>

	<p>Pulsart – это атмосфера таинственности, отсутствие границ и привязанности к конкретному месту. Наша главная задача – эксперименты в области электронной музыки и современного искусства.</p>

	<p>Специальным гостем первого Pulsart станет голландский диджей и продюсер Joris Voorn – босс лейблов Green и Rejected. Йорис в деле уже почти 20 лет. Балансируя между гипнотическим техно-звучанием и мелодичным прогрессив-хаусом, он быстро привлек внимание ведущих диджеев мира. В послужном списке голландца числятся: участие в культовой микс-серии Balance, гостевой Essential Mix для Пита Тонга, выступления практически во всех клубах Ибицы и многое другое. Ворн попал в список 100 лучших диджеев мира по версии Resident Advisor.</p>

	<p>Ссобытие состоится 13 октября, а местом встречи станет новое двухуровневое Art пространство Paveletsky. Вместимость до 4000 человек идеально соответствует масштабу и атмосфере мероприятий Pulsart, создаваемых по принципу «смешения музыки и арта».</p>
